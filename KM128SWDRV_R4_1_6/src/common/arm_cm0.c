SVC_CALLBACK pCallbackSVC;

/******************************************************************************
 * public function definitions                                                *
 ******************************************************************************/
void SVC_InstallCallback (uint8 ip, SVC_CALLBACK pCallback)
{
  if (pCallback) { pCallbackSVC = pCallback; FAULT_SetIsr(INT_SVCall,ip); }
}

