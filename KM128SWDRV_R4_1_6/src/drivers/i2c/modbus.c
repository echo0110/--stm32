uint16   crc16(uint8 *puchMsg, uint16 usDataLen) //*puchMsg要进行CRC校验的消息 
{                                               // usDataLen消息中字节数
	uint8 uchCRCHi = 0xFF ;/*高CRC字节初始化*/ 
        
        
        
	uint8 uchCRCLo = 0xFF ; /*低CRC初始化*/
	uint32 uIndex ; 
	while (usDataLen--) 
	{ 
		uIndex =uchCRCHi ^ *puchMsg++ ; //16位的CRC寄存器高8位与第一个8位二进制数据 相异或
		uchCRCHi = uchCRCLo ^ auchCRCHi[uIndex] ; 
		uchCRCLo = auchCRCLo[uIndex] ; 
	} 
	return (uchCRCHi<<8|uchCRCLo) ; 
}//uint16 crc16(uint8 *puchMsg, uint16 usDataLen)


